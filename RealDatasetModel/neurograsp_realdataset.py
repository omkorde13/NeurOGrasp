# ─── PyTorch Dataset ──────────────────────────────────────────────────────
class RealGlassEventDataset(Dataset):
    """
    Real transparent glass dataset with v2e event simulation.

    Each __getitem__:
    1. Load real JPG image
    2. Resize to (H_tgt, W_tgt)
    3. Synthesise motion → 20 warped frames
    4. Simulate events (V2ESimulator)
    5. Encode voxel (10, H, W)
    6. Build targets from GT bbox
    """
    def __init__(self, pairs: List[Dict], annotations: List[Dict],
                 H: int = 128, W: int = 128,
                 n_frames: int = 20, num_bins: int = 5,
                 augment: bool = True):
        self.pairs       = pairs
        self.annotations = annotations
        self.H, self.W   = H, W
        self.n_frames    = n_frames
        self.num_bins    = num_bins
        self._make_sim()
        self.augment     = augment

    def _make_sim(self):
        self.sim = V2ESimulator(threshold_pos=0.15, threshold_neg=0.15,
                                noise_std=0.012, fps=30.0)

    def __len__(self):
        return len(self.pairs)

    def _pick_primary_box(self, ann: Dict):
        """Return the largest bounding box (primary grasp target)."""
        best_box, best_area = ann['boxes'][0], 0
        for box in ann['boxes']:
            area = (box[2] - box[0]) * (box[3] - box[1])
            if area > best_area:
                best_box, best_area = box, area
        return best_box

    def __getitem__(self, idx):
        img_bgr = cv2.imread(self.pairs[idx]['img'])
        ann     = self.annotations[idx]
        orig_H, orig_W = img_bgr.shape[:2]

        # Resize
        img_bgr = cv2.resize(img_bgr, (self.W, self.H))

        # Motion frames → events → voxel
        frames  = generate_motion_frames(img_bgr, n_frames=self.n_frames)
        self.sim.reset()
        events  = self.sim.process_sequence(frames)
        voxel   = events_to_tensor(events, self.H, self.W, self.num_bins)

        # Ground truth from largest box
        xmin, ymin, xmax, ymax = self._pick_primary_box(ann)
        cx  = ((xmin + xmax) / 2) / orig_W
        cy  = ((ymin + ymax) / 2) / orig_H
        bw  = (xmax - xmin) / orig_W
        bh  = (ymax - ymin) / orig_H
        cx  = float(np.clip(cx, 0, 1))
        cy  = float(np.clip(cy, 0, 1))
        bw  = float(np.clip(bw, 0.01, 1))
        bh  = float(np.clip(bh, 0.01, 1))

        # Augmentation
        if self.augment:
            # Horizontal flip
            if random.random() < 0.5:
                voxel = torch.flip(voxel, dims=[-1])
                cx    = 1.0 - cx
            # Vertical flip
            if random.random() < 0.3:
                voxel = torch.flip(voxel, dims=[-2])
                cy    = 1.0 - cy
            # Gaussian noise
            if random.random() < 0.5:
                voxel = (voxel + torch.randn_like(voxel) * random.uniform(0.01, 0.05)).clamp_(0, 1)
            # Temporal bin dropout
            if random.random() < 0.4:
                b = random.randint(0, self.num_bins - 1)
                voxel[b] = 0; voxel[b + self.num_bins] = 0
            # Brightness scale
            if random.random() < 0.4:
                voxel = (voxel * random.uniform(0.7, 1.3)).clamp_(0, 1)

        # Grasp target: cx, cy, z (0.5 default), angle (0.5 = 0°), width=bw
        targets = {
            'bbox' : torch.tensor([cx, cy, bw, bh], dtype=torch.float32),
            'conf' : torch.tensor([1.0], dtype=torch.float32),
            'cls'  : torch.tensor([1.0], dtype=torch.float32),  # transparent
            'grasp': torch.tensor([cx, cy, 0.5, 0.5, bw], dtype=torch.float32),
        }
        return voxel, targets



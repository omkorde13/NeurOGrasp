# ─── Quantitative evaluation: IoU + Grasp metrics ────────────────────────
def compute_iou(pred_box, gt_box, img_W, img_H):
    """pred_box in [cx,cy,w,h] norm. gt_box in [xmin,ymin,xmax,ymax] pixel."""
    px1 = (pred_box[0] - pred_box[2]/2) * img_W
    py1 = (pred_box[1] - pred_box[3]/2) * img_H
    px2 = (pred_box[0] + pred_box[2]/2) * img_W
    py2 = (pred_box[1] + pred_box[3]/2) * img_H

    gx1, gy1, gx2, gy2 = gt_box

    ix1 = max(px1, gx1); iy1 = max(py1, gy1)
    ix2 = min(px2, gx2); iy2 = min(py2, gy2)

    inter = max(0, ix2-ix1) * max(0, iy2-iy1)
    union = ((px2-px1)*(py2-py1) + (gx2-gx1)*(gy2-gy1) - inter)
    return inter / max(union, 1e-6)



"""## Step 4: Event Camera Simulation (v2e)

We simulate a DVS (Dynamic Vision Sensor) by warping each real image with subtle
synthetic motion — mimicking how a glass object would appear to an event camera
when slightly displaced.

**Why simulate motion from a static image?**
Real event cameras only fire on *change*. Since our dataset has individual frames
(not video), we synthesise motion via:
1. Apply small random affine warp to the image → creates a 'before' and 'after'
2. Run v2e on the resulting frame sequence
3. Glass edges + refraction boundaries generate the strongest events
"""

# ─── Motion-augmented frame generator from a static real image ───────────
def generate_motion_frames(img_bgr: np.ndarray, n_frames: int = 20,
                            max_shift: int = 8, max_rot: float = 2.0) -> List[np.ndarray]:
    """
    Synthesise n_frames by applying a smooth random affine trajectory
    to the input BGR image, simulating camera or object motion.
    """
    H, W = img_bgr.shape[:2]
    cx, cy = W / 2, H / 2

    dx = np.linspace(0, random.uniform(-max_shift, max_shift), n_frames)
    dy = np.linspace(0, random.uniform(-max_shift, max_shift), n_frames)
    da = np.linspace(0, random.uniform(-max_rot,   max_rot),   n_frames)
    ds = np.linspace(1.0, random.uniform(0.97, 1.03),          n_frames)

    frames = []
    for i in range(n_frames):
        M  = cv2.getRotationMatrix2D((cx, cy), da[i], ds[i])
        M[0, 2] += dx[i]
        M[1, 2] += dy[i]
        warped = cv2.warpAffine(img_bgr, M, (W, H),
                                flags=cv2.INTER_LINEAR,
                                borderMode=cv2.BORDER_REFLECT)
        frames.append(warped)
    return frames


# ─── V2E Simulator (fully vectorized) ────────────────────────────────────
class V2ESimulator:
    """
    DVS event simulator — converts a BGR frame sequence to events.
    Uses vectorized numpy (no Python pixel loops).

    Each event: (t, x, y, p) where p=+1 (ON) or p=-1 (OFF)
    Returns dict of numpy arrays for fast downstream processing.
    """
    def __init__(self, threshold_pos=0.15, threshold_neg=0.15,
                 noise_std=0.012, fps=30.0):
        self.C_pos     = threshold_pos
        self.C_neg     = threshold_neg
        self.noise_std = noise_std
        self.dt_us     = 1e6 / fps
        self._ref      = None

    def reset(self):
        self._ref = None

    @staticmethod
    def _to_log(frame_bgr: np.ndarray) -> np.ndarray:
        gray = cv2.cvtColor(frame_bgr, cv2.COLOR_BGR2GRAY).astype(np.float32)
        return np.log(gray + 1.0)

    def process_sequence(self, frames: List[np.ndarray]) -> Dict:
        """Process a list of BGR frames. Returns dict of arrays."""
        if not frames:
            return {'t': np.array([]), 'x': np.array([]),
                    'y': np.array([]), 'p': np.array([])}

        log_stack = np.stack([self._to_log(f) for f in frames])  # (N,H,W)
        if self.noise_std > 0:
            log_stack += np.random.randn(*log_stack.shape).astype(np.float32) * self.noise_std

        all_t, all_x, all_y, all_p = [], [], [], []
        ref = log_stack[0].copy()

        for i in range(1, len(log_stack)):
            t_us = i * self.dt_us
            diff = log_stack[i] - ref

            for mask, pol in [(diff >= self.C_pos, 1), (diff <= -self.C_neg, -1)]:
                yx = np.argwhere(mask)
                if len(yx):
                    all_t.append(np.full(len(yx), t_us,  dtype=np.float32))
                    all_y.append(yx[:, 0].astype(np.int32))
                    all_x.append(yx[:, 1].astype(np.int32))
                    all_p.append(np.full(len(yx), pol,   dtype=np.int8))

            fired = np.abs(diff) >= self.C_pos
            ref[fired] = log_stack[i][fired]

        if not all_t:
            return {'t': np.array([]), 'x': np.array([]),
                    'y': np.array([]), 'p': np.array([])}

        return {
            't': np.concatenate(all_t),
            'x': np.concatenate(all_x),
            'y': np.concatenate(all_y),
            'p': np.concatenate(all_p),
        }



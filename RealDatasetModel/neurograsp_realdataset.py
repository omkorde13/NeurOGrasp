"""## Step 7: Loss Functions"""

# ─── Combined NeurOGrasp Loss ─────────────────────────────────────────────
class NeurOGraspLoss(nn.Module):
    """
    Weighted combination of 4 task losses:
    - BBox  : SmoothL1 (robust regression, bounded gradients)
    - Conf  : BCEWithLogits (raw logits, numerically stable)
    - Class : BCEWithLogits (binary: transparent or not)
    - Grasp : SmoothL1 (robot pose regression)

    Weights: bbox=2.0, conf=1.0, cls=0.5, grasp=1.5
    """
    def __init__(self):
        super().__init__()
        self.smooth_l1 = nn.SmoothL1Loss()
        self.bce       = nn.BCEWithLogitsLoss()

    def forward(self, preds: Dict, targets: Dict) -> Dict:
        l_bbox  = 2.0 * self.smooth_l1(preds['bbox'],  targets['bbox'])
        l_conf  = 1.0 * self.bce(       preds['conf'],  targets['conf'])
        l_cls   = 0.5 * self.bce(       preds['cls'],   targets['cls'])
        l_grasp = 1.5 * self.smooth_l1(preds['grasp'], targets['grasp'])
        total   = l_bbox + l_conf + l_cls + l_grasp
        return {'total': total, 'bbox': l_bbox, 'conf': l_conf,
                'cls': l_cls, 'grasp': l_grasp}



# ─── Ablation models ──────────────────────────────────────────────────────
class SNNNoAttention(nn.Module):
    """Baseline: SNN without temporal attention (plain mean)."""
    def __init__(self, n_bins=5, base_ch=24):
        super().__init__()
        self.encoder = SpikingEncoder(n_bins*2, base_ch)
        self.head    = DetectionHead(self.encoder.out_ch)
    def forward(self, v):
        return self.head(self.encoder(v).mean(dim=1))

class SNNSingleBin(nn.Module):
    """Baseline: only first time bin — no temporal information."""
    def __init__(self, n_bins=5, base_ch=24):
        super().__init__()
        self.encoder = SpikingEncoder(n_bins*2, base_ch)
        self.head    = DetectionHead(self.encoder.out_ch)
    def forward(self, v):
        return self.head(self.encoder(v)[:, 0])


def quick_train_ablation(ModelClass, name, n_epochs=8):
    """Train an ablation variant on the real dataset (small epochs)."""
    m    = ModelClass(n_bins=5, base_ch=24).to(device)
    opt  = optim.AdamW(m.parameters(), lr=1e-3, weight_decay=3e-4)
    crit = NeurOGraspLoss()

    val_losses = []
    for ep in range(n_epochs):
        m.train()
        for voxel, targets in train_loader:
            voxel = voxel.to(device)
            tgts  = {k: v.to(device) for k, v in targets.items()}
            opt.zero_grad(set_to_none=True)
            loss = crit(m(voxel), tgts)['total']
            loss.backward()
            torch.nn.utils.clip_grad_norm_(m.parameters(), 1.0)
            opt.step()

        m.eval()
        vl = 0.0
        with torch.no_grad():
            for voxel, targets in val_loader:
                voxel = voxel.to(device)
                tgts  = {k: v.to(device) for k, v in targets.items()}
                vl   += crit(m(voxel), tgts)['total'].item()
        val_losses.append(vl / max(len(val_loader), 1))

    print(f'  {name:<40} final val = {val_losses[-1]:.4f}')
    return val_losses



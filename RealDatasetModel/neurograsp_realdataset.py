"""## Step 6: SNN Architecture — LIF Neurons + Temporal Attention

### Architecture Flow
```
Voxel (10, 128, 128)
  ↓
SpikingEncoder — 5 time bins × shared LIF layers
  Stage1: Conv(2→32) + BN + LIF + MaxPool  → (32, 64, 64)
  Stage2: Conv(32→64) + BN + LIF + MaxPool → (64, 32, 32)
  Stage3: Conv(64→128) + BN + LIF          → (128, 32, 32)
  Stack bins → (5, 128, 32, 32)
  ↓
TemporalAttention — squeeze-excite over time → reweighted bins
  ↓
Temporal mean → (128, 32, 32)
  ↓
DetectionHead — GAP + FC → 4 outputs
  bbox(4) | conf(1) | class(1) | grasp(5)
```
"""

# ─── Surrogate Gradient ───────────────────────────────────────────────────
class SurrogateSpike(torch.autograd.Function):
    """Heaviside forward, fast-sigmoid surrogate backward."""
    @staticmethod
    def forward(ctx, x):
        ctx.save_for_backward(x)
        return (x >= 0).float()

    @staticmethod
    def backward(ctx, grad_output):
        (x,) = ctx.saved_tensors
        grad = 1.0 / (1.0 + torch.abs(x)) ** 2
        return grad_output * grad

surrogate_spike = SurrogateSpike.apply


# ─── LIF Neuron ───────────────────────────────────────────────────────────
class LIFNeuron(nn.Module):
    """
    Leaky Integrate-and-Fire neuron with hard reset.
    v(t) = v(t-1)*(1-1/τ) + x(t)
    spike = 1 if v >= threshold; reset v → 0 after spike
    """
    def __init__(self, tau: float = 2.0, v_thresh: float = 1.0):
        super().__init__()
        self.tau      = tau
        self.v_thresh = v_thresh

    def forward(self, x, v_prev=None):
        if v_prev is None:
            v_prev = torch.zeros_like(x)
        v      = v_prev * (1 - 1 / self.tau) + x
        spikes = surrogate_spike(v - self.v_thresh)
        v_new  = v * (1 - spikes)          # hard reset
        return spikes, v_new


# ─── Spiking Convolutional Block ──────────────────────────────────────────
class SpikingConvBlock(nn.Module):
    """Conv2d → BatchNorm → LIF → (optional MaxPool)"""
    def __init__(self, in_ch: int, out_ch: int, kernel: int = 3,
                 pool: bool = False, tau: float = 2.0):
        super().__init__()
        self.conv     = nn.Conv2d(in_ch, out_ch, kernel, padding=kernel//2, bias=False)
        self.bn       = nn.BatchNorm2d(out_ch)
        self.lif      = LIFNeuron(tau=tau)
        self.pool     = nn.MaxPool2d(2, 2) if pool else nn.Identity()
        self.has_pool = pool

    def forward(self, x, v_prev=None):
        out = self.bn(self.conv(x))
        spikes, v_new = self.lif(out, v_prev)   # v_new at PRE-pool resolution
        return self.pool(spikes), v_new


# ─── Spiking Encoder ──────────────────────────────────────────────────────
class SpikingEncoder(nn.Module):
    """
    Processes T time-bin pairs (ON + OFF) through shared LIF conv layers.
    Membrane state v is carried forward across bins — TRUE temporal memory.
    """
    def __init__(self, in_bins: int = 10, base_ch: int = 32):
        super().__init__()
        self.T          = in_bins // 2
        self.stage1     = SpikingConvBlock(2, base_ch,       pool=True)
        self.stage2     = SpikingConvBlock(base_ch, base_ch*2, pool=True)
        self.stage3     = SpikingConvBlock(base_ch*2, base_ch*4, pool=False)
        self.out_ch     = base_ch * 4

    def forward(self, voxel):
        T = voxel.shape[1] // 2
        v1 = v2 = v3 = None
        outputs = []
        for t in range(T):
            x_t = voxel[:, [t, t + T], :, :]
            s1, v1 = self.stage1(x_t, v1)
            s2, v2 = self.stage2(s1,  v2)
            s3, v3 = self.stage3(s2,  v3)
            outputs.append(s3)
        return torch.stack(outputs, dim=1)   # (B, T, C, H', W')


# ─── Temporal Attention ───────────────────────────────────────────────────
class TemporalAttention(nn.Module):
    """
    Squeeze-and-Excitation over TIME.
    Learns which event-time bins are most informative for detecting glass.
    """
    def __init__(self, n_bins: int, channels: int, ratio: int = 4):
        super().__init__()
        bottleneck = max(n_bins // ratio, 2)
        self.fc1   = nn.Linear(n_bins, bottleneck, bias=False)
        self.fc2   = nn.Linear(bottleneck, n_bins,  bias=False)
        self.bn    = nn.BatchNorm1d(bottleneck)

    def forward(self, x):
        B, T, C, H, W = x.shape
        energy = x.mean(dim=[2, 3, 4])               # (B, T)
        e = F.relu(self.bn(self.fc1(energy)))
        e = torch.sigmoid(self.fc2(e))               # (B, T)
        return x * e.view(B, T, 1, 1, 1)


# ─── Detection + Grasp Head ───────────────────────────────────────────────
class DetectionHead(nn.Module):
    """
    4 parallel output heads after global average pooling:
    - bbox  (4): [cx, cy, w, h] normalised
    - conf  (1): objectness logit
    - cls   (1): class logit (transparent vs opaque, binary)
    - grasp (5): [cx, cy, z, angle, width] for robot control
    """
    def __init__(self, in_channels: int = 128):
        super().__init__()
        self.gap = nn.AdaptiveAvgPool2d(1)
        self.shared = nn.Sequential(
            nn.Linear(in_channels, 256), nn.ReLU(inplace=True),
            nn.Dropout(0.3),
            nn.Linear(256, 128),         nn.ReLU(inplace=True),
        )
        self.bbox_head  = nn.Linear(128, 4)
        self.conf_head  = nn.Linear(128, 1)
        self.cls_head   = nn.Linear(128, 1)   # binary: transparent
        self.grasp_head = nn.Linear(128, 5)

    def forward(self, feat):
        x    = self.gap(feat).flatten(1)
        x    = self.shared(x)
        bbox  = torch.sigmoid(self.bbox_head(x))
        conf  = self.conf_head(x)
        cls   = self.cls_head(x)
        grasp = self.grasp_head(x)
        return {'bbox': bbox, 'conf': conf, 'cls': cls, 'grasp': grasp}


# ─── Full NeurOGrasp SNN ──────────────────────────────────────────────────
class NeurOGraspSNN(nn.Module):
    """Complete pipeline: SpikingEncoder → TemporalAttention → DetectionHead"""
    def __init__(self, n_bins: int = 5, base_ch: int = 32):
        super().__init__()
        self.encoder  = SpikingEncoder(n_bins * 2, base_ch)
        self.attn     = TemporalAttention(n_bins, self.encoder.out_ch)
        self.head     = DetectionHead(self.encoder.out_ch)

    def forward(self, voxel):
        enc      = self.encoder(voxel)        # (B, T, C, H', W')
        attended = self.attn(enc)             # (B, T, C, H', W') — reweighted
        feat     = attended.mean(dim=1)       # (B, C, H', W')
        return self.head(feat)



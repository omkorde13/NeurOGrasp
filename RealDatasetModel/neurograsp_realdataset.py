"""## Step 5: Voxel Grid — Events → Fixed Tensor (10, H, W)

Raw events are variable-length; neural networks need fixed-size tensors.
We bin events into **T=5 time slices × 2 polarities = 10 channels**.
"""

# ─── Voxel Grid Encoder (vectorized) ─────────────────────────────────────
def events_to_tensor(events: Dict, H: int, W: int, num_bins: int = 5) -> torch.Tensor:
    """
    Convert event dict to voxel tensor of shape (2*num_bins, H, W).
    Channels 0..num_bins-1   = ON  events per time bin
    Channels num_bins..2*n-1 = OFF events per time bin
    """
    voxel = np.zeros((2 * num_bins, H, W), dtype=np.float32)

    t_arr = events['t']
    if len(t_arr) == 0:
        return torch.zeros((2 * num_bins, H, W), dtype=torch.float32)

    x_arr, y_arr, p_arr = events['x'], events['y'], events['p']

    # Assign each event to a time bin
    t_min, t_max = float(t_arr.min()), float(t_arr.max())
    duration = max(t_max - t_min, 1e-6)
    bin_idx  = np.clip(
        np.floor((t_arr - t_min) / duration * num_bins).astype(np.int32),
        0, num_bins - 1
    )

    # Bounds check
    valid    = (x_arr >= 0) & (x_arr < W) & (y_arr >= 0) & (y_arr < H)
    on_mask  = (p_arr ==  1) & valid
    off_mask = (p_arr == -1) & valid

    np.add.at(voxel, (bin_idx[on_mask],            y_arr[on_mask],  x_arr[on_mask]),  1.0)
    np.add.at(voxel, (num_bins + bin_idx[off_mask], y_arr[off_mask], x_arr[off_mask]), 1.0)

    # Per-channel normalisation
    mx = voxel.max(axis=(1, 2), keepdims=True)
    voxel /= np.where(mx > 0, mx, 1.0)

    return torch.from_numpy(voxel)


